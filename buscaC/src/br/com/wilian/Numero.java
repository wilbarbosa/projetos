package br.com.wilian;

public class Numero {

	private String numA;
	private String numB;
	private Long numC;

	public Numero() {
		super();
	}

	public String getNumA() {
		return numA;
	}

	public void setNumA(String numA) {
		this.numA = numA;
	}

	public String getNumB() {
		return numB;
	}

	public void setNumB(String numB) {
		this.numB = numB;
	}

	public Long getNumC() {
		return numC;
	}

	public void setNumC(Long numC) {
		this.numC = numC;
	}

}
